"""Risk guardrail for TAR Engine V2.

Three modes:
  1. Runtime: pattern-match each incoming event against domain rules.
     Pure regex, no LLM, millisecond response. Fires on every event.
  2. Static: scan document text (SKILL.md, configs) against the same
     rule set. Used for skill auditing without execution.
  3. Post-session: (Phase 2) full risk scoring after session ends.

Each RealtimeRule has a match_scope ("runtime", "static", "both")
that controls which mode(s) it participates in. Rules default to
"both" for backward compatibility.

The rules come from DomainConfig.realtime_rules plus a set of
universal rules that apply to all domains.
"""

import re
from dataclasses import dataclass
from datetime import datetime
from typing import Any, Dict, List, Optional

from .domain_config import DomainConfig, RealtimeRule


@dataclass
class RiskAlert:
    """A single risk alert generated by realtime checking."""
    rule_name: str
    severity: str   # "info", "warning", "high", "critical"
    message: str
    tool_name: str
    timestamp: str
    details: Dict[str, Any]

    def to_dict(self) -> Dict:
        return {
            "rule": self.rule_name,
            "severity": self.severity,
            "message": self.message,
            "tool": self.tool_name,
            "timestamp": self.timestamp,
            "details": self.details,
        }


